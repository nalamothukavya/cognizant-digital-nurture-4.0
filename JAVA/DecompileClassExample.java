// Simple class to be decompiled using JD-GUI or CFR
public class DecompileClassExample {
    public int square(int x) {
        return x * x;
    }

    public static void main(String[] args) {
        DecompileClassExample obj = new DecompileClassExample();
        System.out.println("Square of 5 is: " + obj.square(5));
    }
}
